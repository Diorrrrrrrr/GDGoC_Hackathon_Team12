type Key = keyof typeof dict.ko;

function detectLang(): Lang {
  if (typeof navigator === 'undefined') return 'ko';
  const tag = navigator.language.toLowerCase();
  if (tag.startsWith('ja')) return 'ja';
  if (tag.startsWith('en')) return 'en';
  return 'ko';
}

export function useT() {
  const [lang, setLang] = useState<Lang>('ko');
  useEffect(() => setLang(detectLang()), []);
  return (k: Key, ...args: (string | number)[]) => {
    const tmpl = (dict[lang] as Record<Key, string>)[k] ?? (dict.ko as Record<Key, string>)[k] ?? k;
    return args.length
      ? tmpl.replace(/\{(\d+)\}/g, (_, i) => String(args[+i] ?? ''))
      : tmpl;
  };
}

export function bodyStateKey(state: string): Key {
  const map: Record<string, Key> = {
    NORMAL: 'bsNormal',
    POSTURAL_ASYMMETRY: 'bsPosturalAsymmetry',
    ARM_DRIFT: 'bsArmDrift',
    SLOW_SLUMP: 'bsSlowSlump',
    IMPACT_FALL: 'bsImpactFall',
    FALLEN: 'bsFallen',
  };
  return map[state] ?? 'bsNormal';
}
